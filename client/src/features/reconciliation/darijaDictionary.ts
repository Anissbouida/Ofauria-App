export function normalizeDarijaKey(name: string): string {
  return name.trim().toUpperCase()
    .replace(/\s+/g, ' ')
    .replace(/[''`]/g, "'");
}

export function getDarija(productName: string): string {
  const key = normalizeDarijaKey(productName);
  if (DARIJA[key]) return DARIJA[key];
  for (const [k, v] of Object.entries(DARIJA)) {
    if (key === k.toUpperCase()) return v;
  }
  return '';
}

/**
 * Fabrique une fonction de recherche : la base (entrees editables) a priorite,
 * le dictionnaire statique ci-dessus sert de repli.
 */
export function makeDarijaLookup(entries: { product_key: string; darija: string }[]): (name: string) => string {
  const db = new Map(entries.map(e => [e.product_key, e.darija]));
  return (name: string) => db.get(normalizeDarijaKey(name)) || getDarija(name);
}
